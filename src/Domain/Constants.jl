"""
    get_intra_K(N::Int; topology::Symbol=:full) -> Matrix{Float64}

振動子数Nとトポロジーに応じた結合行列を取得（Pure関数）

# Arguments
- `N::Int`: 振動子数 (4 or 10)
- `topology::Symbol`: トポロジータイプ (:full, :tree)

# Returns
- `Matrix{Float64}`: N×N結合行列
"""
function get_intra_K(N::Int; topology::Symbol=:full)
    if N == 4
        return copy(INTRA_K_4)
    elseif N == 10
        if topology == :full
            return copy(INTRA_FULL_K10)
        elseif topology == :tree
            return copy(INTRA_TREE_K10)
        else
            error("Unsupported topology=$topology. Use :full or :tree.")
        end
    else
        error("Unsupported N=$N. Only N=4 or N=10 are predefined.")
    end
end

"""
    get_default_external_input(N::Int) -> Vector{Float64}

デフォルトの外部入力ベクトルを生成（Pure関数）

N=4: ノード1がペースメーカー（I=0.8）、残りはI=0.2
N=10: ノード8,9,10がペースメーカー（I=0.8）、残りはI=0.2
      （Nakao論文の設定に準拠）

# Arguments
- `N::Int`: 振動子数

# Returns
- `Vector{Float64}`: 長さNの外部入力ベクトル
"""
function get_default_external_input(N::Int)
    I = fill(I_DEFAULT, N)
    if N == 4
        I[1] = I_PACEMAKER
    elseif N == 10
        # Nakao論文の設定: ノード8,9,10がペースメーカー
        I[8] = I_PACEMAKER
        I[9] = I_PACEMAKER
        I[10] = I_PACEMAKER
    else
        # その他のNでは最初のノードがペースメーカー
        I[1] = I_PACEMAKER
    end
    return I
end

"""
    create_uniform_coupling_tensor(N::Int; strength::Float64=0.0112) -> Array{Float64,3}

一様結合テンソルを生成（Pure関数）

# Arguments
- `N::Int`: 振動子数
- `strength::Float64`: 結合強度（デフォルト: 0.0112）

# Returns
- `Array{Float64,3}`: N×N×N 結合テンソル
"""
function create_uniform_coupling_tensor(N::Int; strength::Float64=0.0112)
    C = zeros(N, N, N)
    for i in 1:N
        for j in 1:N
            C[i, i, j] = strength
        end
    end
    return C
end
